main(List<String> args) {
  int count = 3;
  print(count);

  double price = 1 + 2;
  print(price);

  num n = 5;
  print(n);

  print(n.toString());
  print(3.8.toInt());

  print(3.1415926.round());
  print(3.1415926.toStringAsFixed(4));

  print(10.remainder(4));

  print(10.compareTo(2));
  print(10.gcd(2)); // 最大公约数

  print(1000.toStringAsExponential(2));
}
